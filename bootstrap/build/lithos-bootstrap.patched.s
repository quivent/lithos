// ============================================================
// Symbol Table — DTC code words
// ============================================================
//
// Entry layout (48 bytes per entry):
//   [ 0..31] name   — up to 32 bytes (zero-padded)
//   [32]     nlen   — name length
//   [33]     kind   — 0=comp-arg 1=local-var 2=param 3=buf 4=const 5=label
//   [34..35] reg    — hardware register number (u16)
//   [36..39] offset — stack/buffer offset (u32)
//   [40..43] width  — bit width 8/16/32/64 (u32)
//   [44..47] pad
//
// Scoped: sym-push saves n_entries; sym-pop restores it.
// Lookup is from end backward so inner scope shadows outer.

// ---- sym-reset ( -- )    clear all entries and scope depth
.align 4
code_SYM_RESET:
    adrp    x0, sym_n_entries
    add     x0, x0, :lo12:sym_n_entries
    str     xzr, [x0]
    adrp    x0, sym_scope_depth
    add     x0, x0, :lo12:sym_scope_depth
    str     xzr, [x0]
    NEXT

// ---- sym-push ( -- )     push a new scope
.align 4
code_SYM_PUSH:
    adrp    x0, sym_scope_depth
    add     x0, x0, :lo12:sym_scope_depth
    ldr     x1, [x0]
    cmp     x1, #SCOPE_MAX_DEPTH
    b.ge    1f
    adrp    x2, sym_n_entries
    add     x2, x2, :lo12:sym_n_entries
    ldr     x3, [x2]
    adrp    x4, sym_scope_base
    add     x4, x4, :lo12:sym_scope_base
    str     x3, [x4, x1, lsl #3]
    add     x1, x1, #1
    str     x1, [x0]
1:  NEXT

// ---- sym-pop ( -- )      pop scope, truncate symbol table to saved base
.align 4
code_SYM_POP:
    adrp    x0, sym_scope_depth
    add     x0, x0, :lo12:sym_scope_depth
    ldr     x1, [x0]
    cbz     x1, 1f
    sub     x1, x1, #1
    str     x1, [x0]
    adrp    x2, sym_scope_base
    add     x2, x2, :lo12:sym_scope_base
    ldr     x3, [x2, x1, lsl #3]
    adrp    x4, sym_n_entries
    add     x4, x4, :lo12:sym_n_entries
    str     x3, [x4]
1:  NEXT

// ---- sym-add ( addr u kind reg offset width -- index )
// Adds a symbol entry. Returns new index, or -1 if table is full.
// Note: arg order on stack — addr is deepest, width is TOS.
.align 4
code_SYM_ADD:
    mov     x10, x22                // x10 = width (TOS)
    ldr     x22, [x24], #8
    mov     x9, x22                 // x9  = offset
    ldr     x22, [x24], #8
    mov     x8, x22                 // x8  = reg
    ldr     x22, [x24], #8
    mov     x7, x22                 // x7  = kind
    ldr     x22, [x24], #8
    mov     x6, x22                 // x6  = u (name length)
    ldr     x22, [x24], #8          // x22 = addr
    mov     x5, x22

    adrp    x0, sym_n_entries
    add     x0, x0, :lo12:sym_n_entries
    ldr     x1, [x0]
    cmp     x1, #SYM_MAX_ENTRIES
    b.ge    sym_add_full

    adrp    x2, sym_table
    add     x2, x2, :lo12:sym_table
    mov     x3, #SYM_ENTRY_SIZE
    madd    x2, x1, x3, x2          // x2 = &entry[n_entries]

    // Zero name field (32 bytes)
    stp     xzr, xzr, [x2]
    stp     xzr, xzr, [x2, #16]

    // Clamp name length to 31
    mov     x11, #31
    cmp     x6, x11
    csel    x11, x6, x11, ls

    cbz     x11, 2f
    mov     x12, #0
3:  ldrb    w13, [x5, x12]
    strb    w13, [x2, x12]
    add     x12, x12, #1
    cmp     x12, x11
    b.lt    3b
2:
    strb    w11, [x2, #32]          // nlen
