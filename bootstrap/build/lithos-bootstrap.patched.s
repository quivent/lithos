    bl      do_parse_name
    cbz     x1, interp_done
    stp     x0, x1, [sp, #-16]!
    bl      do_find_word
    cmp     x0, #0
    b.eq    try_number
    ldr     x2, [sp]
    add     sp, sp, #16
    adrp    x3, var_state
    add     x3, x3, :lo12:var_state
    ldr     x3, [x3]
    cbz     x3, exec_word
    tst     x1, #F_IMMEDIATE
    b.ne    exec_word
    str     x0, [x20], #8
    b       interp_loop

