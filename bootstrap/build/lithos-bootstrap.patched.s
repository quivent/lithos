    adrp    x1, exec_pad
    add     x1, x1, :lo12:exec_pad
    str     x0, [x1]
    adrp    x2, interp_return_xt
    add     x2, x2, :lo12:interp_return_xt
    str     x2, [x1, #8]
    RPUSH   x26
    mov     x26, x1
    NEXT

.align 4
interp_return_xt:
    .quad   interp_return_code
interp_return_code:
    RPOP    x26
    b       interp_loop

try_number:
    ldp     x0, x1, [sp], #16
    stp     x0, x1, [sp, #-16]!
    bl      do_number
    cbz     x1, not_found
    add     sp, sp, #16
    adrp    x3, var_state
    add     x3, x3, :lo12:var_state
    ldr     x3, [x3]
    cbz     x3, push_number
    adrp    x1, cfa_LIT
    add     x1, x1, :lo12:cfa_LIT
    str     x1, [x20], #8
    str     x0, [x20], #8
    b       interp_loop

push_number:
    str     x22, [x24, #-8]!
    mov     x22, x0
    b       interp_loop

not_found:
    ldp     x0, x1, [sp], #16
    adrp    x3, var_state
    add     x3, x3, :lo12:var_state
    ldr     x3, [x3]
    cbnz    x3, compile_latebind
    // Fatal: undefined word
    stp     x0, x1, [sp, #-16]!
    adrp    x1, err_undef
    add     x1, x1, :lo12:err_undef
    mov     x2, #2
    mov     x0, #2
    mov     x8, #SYS_WRITE
    svc     #0
    ldp     x0, x1, [sp], #16
    mov     x2, x1
    mov     x1, x0
    mov     x0, #2
    mov     x8, #SYS_WRITE
    svc     #0
    adrp    x1, newline
    add     x1, x1, :lo12:newline
    mov     x2, #1
    mov     x0, #2
    mov     x8, #SYS_WRITE
    svc     #0
    mov     x0, #1
    mov     x8, #SYS_EXIT
    svc     #0

