// ============================================================
// Lithos control flow compilation — indentation-based blocks
// ============================================================
//
// The .ls language uses indentation for blocks (like Python).
// These words implement the branch-patching system for the
// Lithos parser. The parser tracks indentation level and calls
// these words to emit/patch branches as blocks open and close.
//
// Design:
//   - Forward branches are emitted with a placeholder offset (zero).
//     The placeholder ADDRESS is pushed onto the data stack as a
//     "fixup". When the block closes, the fixup is popped and
//     patched: stored-offset = HERE - fixup_addr.
//   - Back-edges store HERE as a "loop-top" on the data stack
//     before emitting the body. On close, emit BRANCH with
//     offset = loop-top - HERE (negative).
//   - Labels: LS-LABEL snapshots HERE; LS-PATCH-GOTO patches an
//     earlier fixup to that address. Forward references are
//     tracked by the parser's label table.
//   - Indent stack (indent_stack / var_indent_sp): parser pushes
//     expected indent on block open, pops on close.
//   - Loop stack (loop_stack / var_loop_sp): for/while push their
//     loop-top so continue finds the back-edge target.
//
// All compile-time words are IMMEDIATE and operate on the data
// stack at compile time, matching the Forth IF/THEN/BEGIN/WHILE
// idiom already implemented above.

// ------------------------------------------------------------
// Runtime words — executed when the compiled word runs
// ------------------------------------------------------------

