    add     x2, x2, :lo12:cfa_LATEBIND
    str     x2, [x20], #8
    strb    w1, [x20], #1
    mov     x3, x0
    mov     x4, x1
1:  cbz     x4, 2f
    ldrb    w5, [x3], #1
    strb    w5, [x20], #1
    sub     x4, x4, #1
    b       1b
2:  add     x20, x20, #7
    and     x20, x20, #~7
