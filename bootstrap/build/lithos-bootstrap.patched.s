// ============================================================
// Lithos lexer — UTF-8 token handling and string comparison
// ============================================================
//
// Token type constants (returned in x0 by classify/lookup routines):
//   TOK_NONE     0   not a recognized token
//   TOK_LOAD     1   → (E2 86 92)
//   TOK_STORE    2   ← (E2 86 90)
//   TOK_REGRD    3   ↑ (E2 86 91)
//   TOK_REGWR    4   ↓ (E2 86 93)
//   TOK_IDENT    5   plain identifier / unrecognized keyword
//   TOK_SUM      6   Σ (CE A3)
//   TOK_MAX      7   △ (E2 96 B3)
//   TOK_MIN      8   ▽ (E2 96 BD)
//   TOK_SQRT     9   √ (E2 88 9A)
//   TOK_SIN     10   ≅ (E2 89 85)
//   TOK_COS     11   ≡ (E2 89 A1)
//   TOK_NUMBER  12   integer literal
//   TOK_FLOAT   13   float literal
//   TOK_KW_FOR  14   for
//   TOK_KW_EACH 15   each
//   TOK_KW_IF   16   if== if>= if<
//   TOK_KW_TRAP 17   trap
//   TOK_KW_STRIDE 18 stride

.equ TOK_NONE,    0
.equ TOK_LOAD,    1
.equ TOK_STORE,   2
.equ TOK_REGRD,   3
.equ TOK_REGWR,   4
.equ TOK_IDENT,   5
.equ TOK_SUM,     6
.equ TOK_MAX,     7
.equ TOK_MIN,     8
.equ TOK_SQRT,    9
.equ TOK_SIN,    10
.equ TOK_COS,    11
.equ TOK_NUMBER, 12
.equ TOK_FLOAT,  13
.equ TOK_KW_FOR,    14
.equ TOK_KW_EACH,   15
.equ TOK_KW_IF,     16
.equ TOK_KW_TRAP,   17
.equ TOK_KW_STRIDE, 18

// ------------------------------------------------------------
// utf8_classify — classify a UTF-8 byte sequence as a Lithos token
//
// Input:  x0 = pointer to first byte
// Output: x0 = token type (TOK_* or 0 if not a Unicode operator)
//         x1 = byte length consumed (2 or 3), or 0 if not recognized
// Clobbers: x2, x3, x4
// ------------------------------------------------------------
.align 4
utf8_classify:
    ldrb    w2, [x0]            // first byte

    // 2-byte sequence: CE xx  (Σ = CE A3)
    cmp     w2, #0xCE
    b.ne    .Lutf8_3byte
    ldrb    w3, [x0, #1]
    cmp     w3, #0xA3
    b.ne    .Lutf8_none
    mov     x0, #TOK_SUM
    mov     x1, #2
    ret

.Lutf8_3byte:
    // 3-byte sequences all start with E2
    cmp     w2, #0xE2
    b.ne    .Lutf8_none
    ldrb    w3, [x0, #1]       // second byte
    ldrb    w4, [x0, #2]       // third byte

    // Arrows: E2 86 {90,91,92,93}
    cmp     w3, #0x86
    b.ne    .Lutf8_not_arrow
    cmp     w4, #0x92           // → load
    b.eq    .Lutf8_load
    cmp     w4, #0x90           // ← store
    b.eq    .Lutf8_store
    cmp     w4, #0x91           // ↑ regrd
    b.eq    .Lutf8_regrd
    cmp     w4, #0x93           // ↓ regwr
    b.eq    .Lutf8_regwr
    b       .Lutf8_none

.Lutf8_load:
    mov     x0, #TOK_LOAD
    mov     x1, #3
    ret
.Lutf8_store:
    mov     x0, #TOK_STORE
    mov     x1, #3
    ret
.Lutf8_regrd:
    mov     x0, #TOK_REGRD
    mov     x1, #3
    ret
.Lutf8_regwr:
    mov     x0, #TOK_REGWR
    mov     x1, #3
    ret

.Lutf8_not_arrow:
    // Triangles: E2 96 {B3, BD}
    cmp     w3, #0x96
    b.ne    .Lutf8_not_tri
    cmp     w4, #0xB3           // △ max
    b.eq    .Lutf8_max
    cmp     w4, #0xBD           // ▽ min
    b.eq    .Lutf8_min
    b       .Lutf8_none

.Lutf8_max:
    mov     x0, #TOK_MAX
    mov     x1, #3
    ret
.Lutf8_min:
    mov     x0, #TOK_MIN
    mov     x1, #3
    ret

.Lutf8_not_tri:
    // Math: E2 88 9A = √
    cmp     w3, #0x88
    b.ne    .Lutf8_not_math
    cmp     w4, #0x9A           // √ sqrt
    b.ne    .Lutf8_none
    mov     x0, #TOK_SQRT
    mov     x1, #3
    ret

.Lutf8_not_math:
    // Trig: E2 89 {85, A1}
    cmp     w3, #0x89
    b.ne    .Lutf8_none
    cmp     w4, #0x85           // ≅ sin
    b.eq    .Lutf8_sin
    cmp     w4, #0xA1           // ≡ cos
    b.eq    .Lutf8_cos
    b       .Lutf8_none

.Lutf8_sin:
    mov     x0, #TOK_SIN
    mov     x1, #3
    ret
.Lutf8_cos:
    mov     x0, #TOK_COS
    mov     x1, #3
    ret

.Lutf8_none:
    mov     x0, #TOK_NONE
    mov     x1, #0
    ret

// ------------------------------------------------------------
// str_compare — compare two byte spans, case-sensitive
//
// Input:  x0 = addr1, x1 = len1, x2 = addr2, x3 = len2
// Output: x0 = 0 if equal, nonzero otherwise
// Clobbers: x4, x5, x6
// ------------------------------------------------------------
.align 4
str_compare:
    cmp     x1, x3
    b.ne    .Lstrcmp_ne
    cbz     x1, .Lstrcmp_eq     // both empty => equal
    mov     x4, #0
.Lstrcmp_loop:
    ldrb    w5, [x0, x4]
    ldrb    w6, [x2, x4]
    cmp     w5, w6
    b.ne    .Lstrcmp_ne
    add     x4, x4, #1
    cmp     x4, x1
    b.lt    .Lstrcmp_loop
.Lstrcmp_eq:
    mov     x0, #0
    ret
.Lstrcmp_ne:
    mov     x0, #1
    ret

// ------------------------------------------------------------
// keyword_lookup — check an identifier against Lithos keyword table
//
// Input:  x0 = addr, x1 = len
// Output: x0 = token type (TOK_KW_* or TOK_IDENT if not a keyword)
// Clobbers: x2-x9, x30
// ------------------------------------------------------------
.align 4
keyword_lookup:
    stp     x30, xzr, [sp, #-16]!
    stp     x19, x20, [sp, #-16]!
    mov     x19, x0             // save addr
    mov     x20, x1             // save len

    adrp    x6, kw_table
    add     x6, x6, :lo12:kw_table
.Lkw_loop:
    ldrb    w7, [x6]            // keyword length (0 = end sentinel)
    cbz     w7, .Lkw_not_found
    ldrh    w8, [x6, #2]        // token type (halfword at offset 2)
    add     x2, x6, #4          // keyword string starts at offset 4
    mov     x3, x7              // keyword length
    mov     x0, x19
    mov     x1, x20
    bl      str_compare
    cbz     x0, .Lkw_found
    // advance to next entry: 4-byte header + string len, aligned to 4
    ldrb    w7, [x6]
    add     x6, x6, #4
    add     x6, x6, x7
    add     x6, x6, #3
    and     x6, x6, #~3
    b       .Lkw_loop

.Lkw_found:
    mov     x0, x8
    ldp     x19, x20, [sp], #16
    ldp     x30, xzr, [sp], #16
    ret

.Lkw_not_found:
    mov     x0, #TOK_IDENT
    ldp     x19, x20, [sp], #16
    ldp     x30, xzr, [sp], #16
    ret

// ------------------------------------------------------------
// number_parse — parse decimal or hex integer from a byte span
//
// Input:  x0 = addr, x1 = len
// Output: x0 = parsed value
//         x1 = 1 if success, 0 if failure
// Clobbers: x2-x8
//
// Accepts: [-]digits, 0xHEX, 0XHEX, $HEX  (same as do_number)
// This is a thin wrapper that delegates to the existing do_number.
// ------------------------------------------------------------
.align 4
number_parse:
    b       do_number           // tail-call — same ABI

// ------------------------------------------------------------
// float_parse — parse a float literal, return IEEE 754 double bits
//
// Input:  x0 = addr, x1 = len
// Output: x0 = IEEE 754 double bits (64-bit)
//         x1 = 1 if success, 0 if failure
// Clobbers: x2-x10, d0-d2
//
// Format: [-]digits.digits  (no exponent — Lithos constants are simple)
//         Returns the double bit-pattern in x0 (use fmov d0,x0 to load).
// ------------------------------------------------------------
.align 4
float_parse:
    stp     x30, xzr, [sp, #-16]!
    mov     x2, x0              // addr cursor
    mov     x3, x1              // remaining len
    cbz     x3, .Lfp_fail

    // check sign
    mov     x4, #0              // negative flag
    ldrb    w5, [x2]
    cmp     w5, #'-'
    b.ne    .Lfp_int_part
    mov     x4, #1
    add     x2, x2, #1
    sub     x3, x3, #1
    cbz     x3, .Lfp_fail

    // integer part
.Lfp_int_part:
    mov     x6, #0              // integer accumulator
.Lfp_int_loop:
    cbz     x3, .Lfp_no_dot
    ldrb    w5, [x2]
    cmp     w5, #'.'
    b.eq    .Lfp_dot
    sub     w7, w5, #'0'
    cmp     w7, #9
    b.hi    .Lfp_no_dot         // non-digit, no dot => try as int-only
    mov     x8, #10
    mul     x6, x6, x8
    add     x6, x6, x7
    add     x2, x2, #1
    sub     x3, x3, #1
    b       .Lfp_int_loop

.Lfp_dot:
    add     x2, x2, #1         // skip '.'
    sub     x3, x3, #1
    cbz     x3, .Lfp_no_frac   // trailing dot, treat as .0

    // fractional part
    mov     x7, #0              // frac accumulator
    mov     x9, #1              // frac divisor
.Lfp_frac_loop:
    cbz     x3, .Lfp_assemble
    ldrb    w5, [x2]
    sub     w8, w5, #'0'
    cmp     w8, #9
    b.hi    .Lfp_assemble       // non-digit ends fraction
    mov     x10, #10
    mul     x7, x7, x10
    add     x7, x7, x8
    mul     x9, x9, x10
    add     x2, x2, #1
    sub     x3, x3, #1
    b       .Lfp_frac_loop

.Lfp_no_frac:
    mov     x7, #0
    mov     x9, #1
    b       .Lfp_assemble

.Lfp_no_dot:
    // no decimal point found — not a float
    b       .Lfp_fail

.Lfp_assemble:
    // d0 = (double)x6 + (double)x7 / (double)x9
    ucvtf   d0, x6              // integer part
    ucvtf   d1, x7              // frac numerator
    ucvtf   d2, x9              // frac denominator
    fdiv    d1, d1, d2
    fadd    d0, d0, d1

    // apply sign
    cbz     x4, .Lfp_done
    fneg    d0, d0

.Lfp_done:
    fmov    x0, d0              // return IEEE 754 bits in x0
    mov     x1, #1
    ldp     x30, xzr, [sp], #16
    ret

.Lfp_fail:
    mov     x0, #0
    mov     x1, #0
    ldp     x30, xzr, [sp], #16
    ret

